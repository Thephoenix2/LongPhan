    private void ThemSPtbCTHD() {
        DefaultTableModel tbModel = (DefaultTableModel) tbl_CThoadon.getModel();
        Object obj[] = new Object[6];
        int vitri = tbl_SP.getSelectedRow();
        int cacdong = tbl_CThoadon.getRowCount();

        if (Integer.parseInt(tbl_SP.getValueAt(vitri, 2).toString()) == 0) {
            System.out.println(Integer.parseInt(tbl_SP.getValueAt(vitri, 2).toString()));
            btnChon.setEnabled(false);
            spnSoLuong.setModel(new javax.swing.SpinnerNumberModel(0, 0, 0, 0));
        }
        if (tbl_CThoadon.getRowCount() == 0) {
            obj[0] = tbModel.getRowCount() + 1;
            obj[1] = tbl_SP.getValueAt(vitri, 0).toString();
            obj[2] = tbl_SP.getValueAt(vitri, 1).toString();
            obj[3] = soluongchon;
            obj[4] = tbl_SP.getValueAt(vitri, 3).toString();
            obj[5] = BLL_ChuyenDoi.DinhDangSo(BLL_ChuyenDoi.ChuyenSangSo(tbl_SP.getValueAt(vitri, 3).toString()) * soluongchon);
            tbModel.addRow(obj);

        } else if (tbl_CThoadon.getRowCount() > 0) {
            for (int i = 0; i < cacdong; i++) {
                String masp = tbl_CThoadon.getValueAt(i, 1).toString();
                int soluonghientai = Integer.parseInt(tbl_CThoadon.getValueAt(i, 3).toString());

                if (MaSP.equals(masp)) {
                    tbl_CThoadon.setValueAt(soluonghientai + soluongchon, i, 3);
                    tbl_CThoadon.setValueAt(BLL_ChuyenDoi.DinhDangSo(BLL_ChuyenDoi.ChuyenSangSo(tbl_SP.getValueAt(vitri, 3).toString()) * (soluonghientai + soluongchon)), i, 5);
                    BLL_ChuyenDoi.DinhDangSo(BLL_ChuyenDoi.ChuyenSangSo(tbl_SP.getValueAt(vitri, 3).toString()) * (soluonghientai + soluongchon));

                    return;
                }

            }

            obj[0] = tbModel.getRowCount() + 1;
            obj[1] = tbl_SP.getValueAt(vitri, 0).toString();
            obj[2] = tbl_SP.getValueAt(vitri, 1).toString();
            obj[3] = soluongchon;
            obj[4] = tbl_SP.getValueAt(vitri, 3).toString();
            obj[5] = BLL_ChuyenDoi.DinhDangSo(BLL_ChuyenDoi.ChuyenSangSo(tbl_SP.getValueAt(vitri, 3).toString()) * soluongchon);
            tbModel.addRow(obj);

        }

    }

