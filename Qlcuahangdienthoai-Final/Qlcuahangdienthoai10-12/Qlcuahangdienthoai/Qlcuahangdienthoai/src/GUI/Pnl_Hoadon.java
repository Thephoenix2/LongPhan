    private void UpdateLoaiKH() {
        My_CBB mb = (My_CBB) cbbKhachHang.getSelectedItem();
        ResultSet rs = DAO.DAO_HoaDon.getTongTientheoMaKH(mb.value.toString());
        try {
            while (rs.next()) {
                if (rs.getDouble("TongTien") > 10000000 && rs.getDouble("TongTien") < 50000000) {
                    DAO.DAO_HoaDon.UpdateLoaiKH(mb.value.toString(), "Silver");
                } else if (rs.getDouble("TongTien") >= 50000000 && rs.getDouble("TongTien") < 100000000) {
                    DAO.DAO_HoaDon.UpdateLoaiKH(mb.value.toString(), "Gold");
                } else if (rs.getDouble("TongTien") >= 100000000) {
                    DAO.DAO_HoaDon.UpdateLoaiKH(mb.value.toString(), "Platinum");
                }
            }
        } catch (SQLException ex) {
            ThongBao.ThongBao("Lỗi", "Thông báo");
        }
    }

