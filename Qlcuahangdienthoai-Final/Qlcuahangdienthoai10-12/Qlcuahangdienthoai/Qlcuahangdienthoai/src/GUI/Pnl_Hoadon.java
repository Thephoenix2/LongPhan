    private double TinhTien() {
        double tongTien = 0;
        giamgia = 0;
        for (int i = 0; i < tbl_CThoadon.getRowCount(); i++) {
            My_CBB mb = (My_CBB) cbbKhachHang.getSelectedItem();
            ResultSet rs = DAO.DAO_HoaDon.GetKHTheoMa(mb.value.toString());
            try {
                //tongTien += BLL_ChuyenDoi.ChuyenSangSo(tbl_CThoadon.getValueAt(i, 5).toString());

                while (rs.next()) {
                    if (rs.getString("LoaiKhachHang").equals("Silver")) {
                        tongTien += BLL_ChuyenDoi.ChuyenSangSo(tbl_CThoadon.getValueAt(i, 5).toString()) - (BLL_ChuyenDoi.ChuyenSangSo(tbl_CThoadon.getValueAt(i, 5).toString()) * 5 / 100);
                        giamgia += BLL_ChuyenDoi.ChuyenSangSo(tbl_CThoadon.getValueAt(i, 5).toString()) * 5 / 100;
                    } else if (rs.getString("LoaiKhachHang").equals("Gold")) {
                        tongTien += BLL_ChuyenDoi.ChuyenSangSo(tbl_CThoadon.getValueAt(i, 5).toString()) - (BLL_ChuyenDoi.ChuyenSangSo(tbl_CThoadon.getValueAt(i, 5).toString()) * 10 / 100);
                        giamgia += BLL_ChuyenDoi.ChuyenSangSo(tbl_CThoadon.getValueAt(i, 5).toString()) * 10 / 100;
                    } else if (rs.getString("LoaiKhachHang").equals("Platinum")) {
                        tongTien += BLL_ChuyenDoi.ChuyenSangSo(tbl_CThoadon.getValueAt(i, 5).toString()) - (BLL_ChuyenDoi.ChuyenSangSo(tbl_CThoadon.getValueAt(i, 5).toString()) * 15 / 100);
                        giamgia += BLL_ChuyenDoi.ChuyenSangSo(tbl_CThoadon.getValueAt(i, 5).toString()) * 15 / 100;
                    } else {
                        tongTien += BLL_ChuyenDoi.ChuyenSangSo(tbl_CThoadon.getValueAt(i, 5).toString());
                        giamgia = 0;
                    }
                }
            } catch (SQLException ex) {

            }
        }

        return tongTien;
    }
    private void btnThanhToanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThanhToanActionPerformed

        DTO.DTO_HoaDon HD = new DTO_HoaDon();
        HD.setMaHD(Integer.parseInt(txtMaHD.getText()));
        My_CBB mb = (My_CBB) cbbKhachHang.getSelectedItem();
        String MaKH = mb.value.toString();
        HD.setMaKH(MaKH);
        HD.setMoTa(txtGhiChu.getText());
        HD.setNgayLap(txtNgayTao.getText());
        HD.setTenTK(Frm_Dangnhap.us);
        HD.setGiamGia(BLL_ChuyenDoi.ChuyenSangSo(txtGiamGia.getText()));
        double tongTien = BLL_ChuyenDoi.ChuyenSangSo(txtTongCong.getText());
        HD.setTongTien(tongTien);
        DAO.DAO_HoaDon.ThemHD(HD);

        DTO.DTO_CTHD CTHD = new DTO_CTHD();
        for (int i = 0; i < tbl_CThoadon.getRowCount(); i++) {
            ResultSet rsMaHD = DAO.DAO_HoaDon.GetMaHDMax();
            try {
                while (rsMaHD.next()) {
                    CTHD.setMaHD(rsMaHD.getInt("MaHD"));
                }
            } catch (SQLException ex) {

            }
            CTHD.setMaSP(tbl_CThoadon.getValueAt(i, 1).toString());
            CTHD.setMoTa(txtGhiChu.getText());
            CTHD.setSoLuong(Integer.parseInt(tbl_CThoadon.getValueAt(i, 3).toString()));
            CTHD.setThanhTien(BLL_ChuyenDoi.ChuyenSangSo(tbl_CThoadon.getValueAt(i, 5).toString()));
            DAO.DAO_CTHD.ThemCTHD(CTHD);
            ResultSet rs = DAO.DAO_HoaDon.getSLSP(tbl_CThoadon.getValueAt(i, 1).toString());
            try {
                while (rs.next()) {
                    int SLKho = rs.getInt("TonKho");
                    DAO.DAO_HoaDon.UpdateSLSP(SLKho, Integer.parseInt(tbl_CThoadon.getValueAt(i, 3).toString()),
                            tbl_CThoadon.getValueAt(i, 1).toString());
                }
            } catch (SQLException ex) {

            }

        }
        UpdateLoaiKH();
        XuatHoaDon(CTHD.getMaHD());
        LamMoi();


    }//GEN-LAST:event_btnThanhToanActionPerformed

