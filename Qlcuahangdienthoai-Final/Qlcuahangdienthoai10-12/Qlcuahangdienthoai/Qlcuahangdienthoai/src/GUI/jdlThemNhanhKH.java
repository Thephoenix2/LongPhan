    private void btnXacNhanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXacNhanActionPerformed
        DTO.DTO_Khachhang KH = new DTO_Khachhang();
        KH.setMaKH(txtMaKH.getText());
        KH.setTenKH(txtTenKH.getText());
        if (cbbGioiTinh.getSelectedItem().equals("Nam")) {
            KH.setGioitinh(true);
        } else {
            KH.setGioitinh(false);
        }
        try {
            KH.setNgaysinh(BLL_ChuyenDoi.GetDate(jdcNgaySinh.getDate())); 
        } catch (Exception e) {
        }
        KH.setEmail(txtEmail.getText());
        KH.setSdt(txtSDT.getText());
        KH.setDiachi(txtDiaChi.getText());
        KH.setLoaiKh(cbbLoaiKH.getSelectedItem().toString());
        KH.setHinh("");
        KH.setMota("");
        if (KiemTra(KH)) {
            ThongBao.ThongBao("Thêm thành công", "Thông báo");
            DAO.DAO_Khachhang.ThemKH(KH);
            BLL.BLL_HoaDon.DuDuLieucbbKH(Pnl_Hoadon.cbbKhachHang);
            this.dispose();
        }
    }//GEN-LAST:event_btnXacNhanActionPerformed
    private static boolean KiemTra(DTO.DTO_Khachhang KH) {
        if (KH.getMaKH().trim().length() < 1) {
            ThongBao.ThongBao("Mã KH không được để trống", "Thông báo");
            return false;
        }
        ResultSet rs = DAO.DAO_Khachhang.getTheoMa(KH.getMaKH());
        try {
            if (rs.next()) {
                ThongBao.ThongBao("Mã khách hàng đã được sử dụng", "Thông báo");
                return false;
            }
        } catch (SQLException ex) {
            Logger.getLogger(jdlThemNhanhKH.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (KH.getTenKH().trim().length() < 5) {
            ThongBao.ThongBao("Tên khách hàng tối thiểu 5 kí tự", "Thông báo");
            return false;
        }
        if (KH.getNgaysinh() == null) {
            ThongBao.ThongBao("Ngày sinh không được để trống", "Thông báo");
            return false;
        }
        String KTEmail = "\\w+@\\w+(\\.\\w+){1,2}";
        if (!KH.getEmail().matches(KTEmail)) {
            ThongBao.ThongBao("Email không hợp lệ", "Thông báo");
            return false;
        }
        ResultSet rs1 = DAO.DAO_Khachhang.GetALL();
        try {
            while(rs1.next()){
                if (KH.getEmail().equals(rs1.getString("Email"))) {
                    ThongBao.ThongBao("Email đã được sử dụng", "Thông báo");
                    return false;
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(jdlThemNhanhKH.class.getName()).log(Level.SEVERE, null, ex);
        }
        String KTSDT = "\\d{10}";
        if (!KH.getSdt().matches(KTSDT)) {
            ThongBao.ThongBao("SĐT không hợp lệ", "Thông báo");
            return false;
        }
        if (KH.getDiachi().trim().length() < 5) {
            ThongBao.ThongBao("Địa chỉ tối thiểu 5 kí tự", "Thông báo");
            return false;
        }
        return true;
    }

