    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        if (tblSanpham.getSelectedRowCount() != 1) {
            ThongBao.ThongBao("Bạn chưa chọn sản phẩm để xóa", "Thông báo");
            return;
        } else {
            int kq = JOptionPane.showConfirmDialog(new JFrame(), "Bạn có chắc chắn muốn xóa không",
                    "Thông báo", JOptionPane.YES_NO_OPTION);
            if (kq == JOptionPane.YES_OPTION) {
                int cacdong[] = tblSanpham.getSelectedRows();
                for (int i = 0; i < cacdong.length; i++) {
                    String sanpham = tblSanpham.getValueAt(cacdong[i], 1).toString();
                    int kq1 = DAO.DAO_SanPham.XoaSP(sanpham);
                    if (kq1 < 1) {
                        ThongBao.ThongBao("Sản phẩm này đang có dữ liệu", "Thông báo");
                        return;
                    }
                }
            }
            BLL.BLL_SanPham.DoDuLieu(tblSanpham);
        }
    }//GEN-LAST:event_btnXoaActionPerformed

