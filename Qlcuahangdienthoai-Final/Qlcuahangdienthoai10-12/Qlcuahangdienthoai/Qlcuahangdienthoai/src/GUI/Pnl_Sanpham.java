    private void lblImageMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblImageMouseClicked
        JFileChooser file = new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter("*.Images", "jpg", "gif", "png");
        file.addChoosableFileFilter(filter);
        int resuft = file.showOpenDialog(null);
        if (resuft == JFileChooser.APPROVE_OPTION) {
            f = file.getSelectedFile();
            imageNameSP = f.getAbsolutePath();
            ImageIcon ii = new ImageIcon(new ImageIcon(imageNameSP).getImage().getScaledInstance(lblImage.getWidth(), lblImage.getHeight(), Image.SCALE_SMOOTH));
            lblImage.setIcon(ii);
            try {
                imageNameSP = f.getName();
            } catch (Exception e) {
            }
        }
    }//GEN-LAST:event_lblImageMouseClicked

