    private void LoaDataCBBLoaiSP() {
        DefaultComboBoxModel cbbModel = (DefaultComboBoxModel) cbbLoaisp.getModel();
        ResultSet rs = DAO.DAO_loaiSanPham.GetALL();
        try {
            while (rs.next()) {
                Object tenLoai = rs.getString("TenLoaiSP");
                Object maLoai = rs.getString("MaLoaiSP");
                My_CBB mb = new My_CBB(maLoai, tenLoai);
                cbbModel.addElement(mb);
            }
        } catch (SQLException ex) {
            ThongBao.ThongBao("Lỗi lấy dữ liệu", "Thông báo");
        }
    }

    public static boolean saveImage(File file) {
        File dir = new File("IMG");
        if (!dir.exists()) {
            dir.mkdirs();
        }

        try {
            File newFile = new File(dir, file.getName());
            Path source = Paths.get(file.getAbsolutePath());
            Path destination = Paths.get(newFile.getAbsolutePath());
            Files.copy(source, destination, StandardCopyOption.REPLACE_EXISTING);
            return true;
        } catch (Exception e) {
            return false;
        }
    }
    private void tblSanphamMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSanphamMouseClicked
        int vitri = tblSanpham.getSelectedRow();
        DefaultComboBoxModel cbbModel = (DefaultComboBoxModel) cbbLoaisp.getModel();
        for (int i = 0; i < cbbLoaisp.getItemCount(); i++) {
            My_CBB mb = (My_CBB) cbbModel.getElementAt(i);
            String loaisp = mb.value.toString();
            if (loaisp.equals((tblSanpham.getValueAt(vitri, 3)))) {
                cbbLoaisp.setSelectedIndex(i);
            }
        }
        txtMasp.setText(tblSanpham.getValueAt(vitri, 1).toString());
        txtTensp.setText(tblSanpham.getValueAt(vitri, 2).toString());
        txtGianhap.setText(tblSanpham.getValueAt(vitri, 4).toString());
        spnTonkho.setValue(Integer.parseInt(tblSanpham.getValueAt(vitri, 6).toString()));
        txtGiaban.setText(tblSanpham.getValueAt(vitri, 5).toString());
        ResultSet rs = DAO.DAO_SanPham.GetMoTa(tblSanpham.getValueAt(vitri, 1).toString());
        try {
            while (rs.next()) {
                txtGhichu.setText(rs.getString("Mota"));
            }
        } catch (SQLException ex) {

        }
//        ImageIcon ii = new ImageIcon(new ImageIcon(tbl_sanpham.getValueAt(vitri, 7).toString()).getImage().getScaledInstance(Lbl_Image.getWidth(), Lbl_Image.getHeight(), Image.SCALE_SMOOTH));
//        Lbl_Image.setIcon(ii);
//        imageNameSP = tbl_sanpham.getValueAt(vitri, 7).toString();
        lbl_ImageSP(tblSanpham.getValueAt(vitri, 7).toString());
    }//GEN-LAST:event_tblSanphamMouseClicked
