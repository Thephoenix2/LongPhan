    private void ptnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ptnThemActionPerformed
        DTO.DTO_LoaiSanPham loaisanPham = new DTO_LoaiSanPham();
        loaisanPham.setMaSP(txtMasp.getText().toString());
        loaisanPham.setTenSP(txtTensp.getText());
        loaisanPham.setGhichu(txtGhichu.getText());
        boolean kq = BLL.BLL_loaiSanPham.KiemTra(loaisanPham);
        if (kq) {
            int so = DAO.DAO_loaiSanPham.ThemSP(loaisanPham);
            if (so > 0) {
                ThongBao.ThongBao("Thêm thành công", "Thông báo");
                BLL.BLL_loaiSanPham.DoDuLieu(tbl_Loaisanpham);
            }

        }
    }//GEN-LAST:event_ptnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        DTO.DTO_LoaiSanPham sanPham = new DTO_LoaiSanPham();
        sanPham.setMaSP(txtMasp.getText());
        sanPham.setTenSP(txtTensp.getText());
        sanPham.setGhichu(txtGhichu.getText());
        boolean kq = BLL.BLL_loaiSanPham.KiemTra(sanPham);
        if (kq) {
            int so = DAO.DAO_loaiSanPham.SuaSP(sanPham);
            if (so > 0) {
                ThongBao.ThongBao("Sửa thành công", "Thông báo");
                BLL.BLL_loaiSanPham.DoDuLieu(tbl_Loaisanpham);
            }
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        if (tbl_Loaisanpham.getSelectedRowCount() != 1) {
            ThongBao.ThongBao("Bạn chưa chọn loại sản phẩm để xóa", "Thông báo");
            return;
        } else {
            int kq = JOptionPane.showConfirmDialog(new JFrame(), "Bạn có chắc chắn muốn xóa không",
                    "Thông báo", JOptionPane.YES_NO_OPTION);
            if (kq == JOptionPane.YES_OPTION) {
                int cacdong[] = tbl_Loaisanpham.getSelectedRows();
                for (int i = 0; i < cacdong.length; i++) {
                    String MaNV = tbl_Loaisanpham.getValueAt(cacdong[i], 1).toString();
                    int kq1 = DAO.DAO_loaiSanPham.XoaSP(MaNV);
                    if (kq1 < 1) {
                        ThongBao.ThongBao("Loại sản phẩm này đang có dữ liệu", "Thông báo");
                        return;
                    }
                }
            }
            BLL.BLL_loaiSanPham.DoDuLieu(tbl_Loaisanpham);
        }
    }//GEN-LAST:event_btnXoaActionPerformed

