    private void tbl_LoaisanphamMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_LoaisanphamMouseClicked
        int vitri = tbl_Loaisanpham.getSelectedRow();
        txtMasp.setText(tbl_Loaisanpham.getValueAt(vitri, 1).toString());
        txtTensp.setText(tbl_Loaisanpham.getValueAt(vitri, 2).toString());
        ResultSet rs = DAO.DAO_loaiSanPham.GetMoTa(tbl_Loaisanpham.getValueAt(vitri, 1).toString());
        try {
            while (rs.next()) {
                txtGhichu.setText(rs.getString("MoTa"));
            }
        } catch (SQLException ex) {

        }

        txtMasp.setFocusable(false);

    }//GEN-LAST:event_tbl_LoaisanphamMouseClicked


