    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        if (tbl_luong.getSelectedRowCount()!= 1) {
             ThongBao.ThongBao("Bạn chưa chọn dòng để xóa", "Thông báo");
            return;
        } else {
               int kq = JOptionPane.showConfirmDialog(new JFrame(), "Bạn có chắc chắn muốn xóa không",
                "Thông báo", JOptionPane.YES_NO_OPTION);
        if (kq == JOptionPane.YES_OPTION) {
            int cacdong[] = tbl_luong.getSelectedRows();
            for (int i = 0; i < cacdong.length; i++) {
                int MaLuong = Integer.parseInt(tbl_luong.getValueAt(cacdong[i], 1).toString());
                DAO.DAO_Luong.Xoa(MaLuong);
            }
        }
        BLL.BLL_Luong.DoDuLieu(tbl_luong);
        LamMoi();
        }
     
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        DTO.DTO_Luong luong = new DTO_Luong();
        int sodongduocchon = tbl_luong.getSelectedRowCount();
        if (sodongduocchon != 1) {
            ThongBao.ThongBao("Bạn cần chọn 1 dòng để sửa", "Thông báo");
            return;
        }
        int vitri = tbl_luong.getSelectedRow();
        My_CBB mb = (My_CBB) cbbNhanVien.getSelectedItem();
        luong.setMaNV(mb.value.toString());
        luong.setMoTa(txtGhiChu.getText());
        luong.setLuong1Gio(BLL_ChuyenDoi.ChuyenSangSo(txtLuong1Gio.getText()));
        luong.setNgayLam(txtNgayLam.getText());
        luong.setSoGioLam(Integer.parseInt(spnSoGioLam.getModel().getValue().toString()));
        luong.setTongLuong1Ngay(BLL_ChuyenDoi.ChuyenSangSo(txtLuong1Ngay.getText()));
        luong.setMaLuong(Integer.parseInt(tbl_luong.getValueAt(vitri, 1).toString()));
        luong.setLuongLamThem(BLL_ChuyenDoi.ChuyenSangSo(txtLuongLamThem.getText()));

//
        boolean kq = BLL.BLL_Luong.KiemTra(luong);
        if (kq) {
            DAO.DAO_Luong.Sua(luong);
            BLL.BLL_Luong.DoDuLieu(tbl_luong);
            LamMoi();
        }
    }//GEN-LAST:event_btnSuaActionPerformed

