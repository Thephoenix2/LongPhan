    private void tbl_luongMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_luongMouseClicked
        int vitri = tbl_luong.getSelectedRow();
        txtMaLuong.setText(tbl_luong.getValueAt(vitri, 1).toString());
        DefaultComboBoxModel cbbModel = (DefaultComboBoxModel) cbbNhanVien.getModel();
        for (int i = 0; i < cbbNhanVien.getItemCount(); i++) {
            My_CBB mb = (My_CBB) cbbModel.getElementAt(i);
            String tenNV = mb.text.toString();
            if (tenNV.equals((tbl_luong.getValueAt(vitri, 2)))) {
                cbbNhanVien.setSelectedIndex(i);
            }
        }
        txtLuong1Gio.setText(tbl_luong.getValueAt(vitri, 3).toString());
        spnSoGioLam.setValue(tbl_luong.getValueAt(vitri, 4));
        txtNgayLam.setText(tbl_luong.getValueAt(vitri, 5).toString());
        txtLuongLamThem.setText(tbl_luong.getValueAt(vitri, 6).toString());
        txtLuong1Ngay.setText(tbl_luong.getValueAt(vitri, 7).toString());
        txtGhiChu.setText(tbl_luong.getValueAt(vitri, 8).toString());
    }//GEN-LAST:event_tbl_luongMouseClicked

