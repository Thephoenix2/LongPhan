    public static void LoaDataCBBNhanVien() {
        cbbNV.removeAllItems();
        cbbNhanVien.removeAllItems();
        DefaultComboBoxModel cbbModel = (DefaultComboBoxModel) cbbNhanVien.getModel();
        DefaultComboBoxModel cbbModel1 = (DefaultComboBoxModel) cbbNV.getModel();
        ResultSet rs = DAO.DAO_NhanVien.GetALL();
        try {
            while (rs.next()) {
                Object ten = rs.getString("TenNV");
                Object ma = rs.getString("MaNV");
                My_CBB mb = new My_CBB(ma, ten);
                cbbModel.addElement(mb);
                cbbModel1.addElement(mb);
            }
        } catch (SQLException ex) {
            ThongBao.ThongBao("Lỗi lấy dữ liệu", "Thông báo");
        }
    }

    private void GetMaLuong() {
        ResultSet rs = DAO.DAO_Luong.GetMaLuong();
        try {
            while (rs.next()) {
                txtMaLuong.setText(String.valueOf(rs.getInt("MaLuong") + 1));
            }
        } catch (SQLException ex) {
            Logger.getLogger(Pnl_Hoadon.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
