    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        DTO.DTO_Khachhang KH = new DTO_Khachhang();
        KH.setMaKH(txtMaKH.getText());
        KH.setTenKH(txtTenKH.getText());
        if (cbbGioitinh.getSelectedItem().equals("Nam")) {
            KH.setGioitinh(true);
        } else {
            KH.setGioitinh(false);
        }
        KH.setDiachi(txtDiachi.getText());
        KH.setSdt(txtSdt.getText());
        KH.setEmail(txtEmail.getText());
        KH.setMota(Txt_Ghichu.getText());
        KH.setLoaiKh(Cbb_LoaiKH.getSelectedItem().toString());
        if (txtNgaysinh.getDate() == null) {
        } else {
            KH.setNgaysinh(BLL_ChuyenDoi.GetDate(txtNgaysinh.getDate()));
        }
        KH.setHinh(imageName);
        boolean kq = BLL.BLL_Khachhang.KiemTraThem(KH);
        if (kq) {
            ThongBao.ThongBao("Thêm thành công", "Thông báo");
            DAO.DAO_Khachhang.ThemKH(KH);
            BLL.BLL_Khachhang.DoDuLieu(Tbl_Khachhang);
        }


    }//GEN-LAST:event_btnThemActionPerformed
