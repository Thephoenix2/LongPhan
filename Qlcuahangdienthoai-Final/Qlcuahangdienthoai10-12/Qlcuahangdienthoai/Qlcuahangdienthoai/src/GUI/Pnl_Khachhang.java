    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        DTO.DTO_Khachhang KH = new DTO_Khachhang();
        int sodongduocchon = Tbl_Khachhang.getSelectedRowCount();
        if (sodongduocchon != 1) {
            ThongBao.ThongBao("Bạn cần chọn 1 dòng để sửa", "Thông báo");
            return;
        }
        KH.setMaKH(txtMaKH.getText());
        KH.setTenKH(txtTenKH.getText());
        if (cbbGioitinh.getSelectedItem().equals("Nam")) {
            KH.setGioitinh(true);
        } else {
            KH.setGioitinh(false);
        }
        KH.setDiachi(txtDiachi.getText());
        KH.setNgaysinh(BLL_ChuyenDoi.GetDate(txtNgaysinh.getDate()));
        KH.setSdt(txtSdt.getText());
        KH.setEmail(txtEmail.getText());
        KH.setMota(Txt_Ghichu.getText());
        KH.setLoaiKh(Cbb_LoaiKH.getSelectedItem().toString());
        KH.setHinh(imageName);
        boolean kq = BLL.BLL_Khachhang.KiemTraSua(KH);
        if (kq) {
            ThongBao.ThongBao("Sửa thành công", "Thông báo");
            DAO.DAO_Khachhang.SuaKH(KH);
            BLL.BLL_Khachhang.DoDuLieu(Tbl_Khachhang);
        }


    }//GEN-LAST:event_btnSuaActionPerformed
    public static boolean saveImage(File file) {
        File dir = new File("IMG");
        if (!dir.exists()) {
            dir.mkdirs();
        }

        try {
            File newFile = new File(dir, file.getName());
            Path source = Paths.get(file.getAbsolutePath());
            Path destination = Paths.get(newFile.getAbsolutePath());
            Files.copy(source, destination, StandardCopyOption.REPLACE_EXISTING);
            return true;
        } catch (Exception e) {
            return false;
        }
    }

